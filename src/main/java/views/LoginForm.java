    private void loginBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginBtnMouseClicked
        String user = username.getText();
        String pass = new String(password.getPassword());

        try {
            User session = authService.login(user, pass);

            if (session != null) {
                if (session.getRole().equals("admin")) {
                    new AdminLayout(session).setVisible(true);
                } else if (session.getRole().equals("kasir")) {
                    new StaffLayout(session).setVisible(true);
                } else if (session.getRole().equals("koki")) {
                    new KokiLayout(session).setVisible(true);
                }
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this,
                        "Username atau password salah!",
                        "Login Gagal",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_loginBtnMouseClicked

